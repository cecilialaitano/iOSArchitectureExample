import UIKit

extension UIColor {

  static func teal() -> UIColor {
    return UIColor(red: 0/255.0, green: 145/255.0, blue: 147/255.0, alpha: 1)
  }

  static func navBarTitleColor() -> UIColor {
    return UIColor(red: 66/255.0, green: 66/255.0, blue: 66/255.0, alpha: 1)
  }
}
